# ============================================================
#  Lab 05 – Euclidean Algorithm (GCD)
# ============================================================
#
#  INPUT:
#    a = 56
#    b = 98
#
#  OUTPUT:
#    GCD(56, 98) = 14
#
# ============================================================

def gcd(a, b):
    while b:
        a, b = b, a % b
    return a

# --- Input ---
a = 56
b = 98

# --- Output ---
print(f"GCD({a}, {b}) = {gcd(a, b)}")   # GCD(56, 98) = 14
